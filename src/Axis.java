public class Axis {

	private double xAxis;
	private double yAxis;
	private int capa;
	
	public double getxAxis() {
		return xAxis;
	}
	public void setxAxis(double xAxis) {
		this.xAxis = xAxis;
	}
	public double getyAxis() {
		return yAxis;
	}
	public void setyAxis(double yAxis) {
		this.yAxis = yAxis;
	}
	public int getcapa() {
		return capa;
	}
	public void setcapa(int capa) {
		this.capa = capa;
	}
	
}
